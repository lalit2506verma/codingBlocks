package Lec1;

public class Assignment_Operator {
    public static void main(String[] args) {
        int x = 6;

        x += 5; // x = x + 5;
        System.out.println(x);
        x -= 5; // x = x - 5;
        x *= 5; // x = x * 5;
        x /= 3; // x = x / 2;
        x %= 2; // x = x % 2;

    }
}
